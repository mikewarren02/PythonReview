# For each loop

family = ["Mike", "Mekhi", "Malik", "Nyeema", "Najae", "Murrie", "Michael"]

for name in family:
    print(name)



# Reverse loop
numbers = [1, 2, 3, 4, 5, 6, 7, 8]

for numbers in range(8, 0, -1):
    print(numbers)


